%% API

-type p_tree_node(K, V) :: nil | {K, V, p_tree_node(K, V), p_tree_node(K, V)}.
-type p_tree() :: p_tree_node(_, _).

-spec empty() -> p_tree().
-export([min_interval/2]).
-export([test/0]).

-spec min_interval(Intervals :: [[integer()]], Queries :: [integer()]) -> [integer()].
min_interval(Intervals, Queries) ->
    Queries1 = lists:sort(loop_queries(Queries, [], 0)),
    R = search_all(Queries1, lists:sort(Intervals), nil, []),
    L = format_res(R, length(Queries)),
    erase(),
    L.

format_res([[V, I] | NextR], L) ->
    put(I, V),
    format_res(NextR, L);
format_res([], L) ->
    create_list(L, []).

create_list(N, R) when N >= 1 ->
    create_list(N - 1, [get(N - 1) | R]);
create_list(_, R) ->
    R.

loop_queries([Q | NextQ], R, I) ->
    loop_queries(NextQ, [[Q, I] | R], I + 1);
loop_queries([], R, _) ->
    %%  io:format("R ~p ~n", [R]),
    lists:reverse(R).

possible(_Q, [], Heap) ->
    {Heap, []};
possible(Q, [[L, R] | NextInterval], Heap) ->
    if Q >= L ->
           possible(Q, NextInterval, insert(R - L + 1, [L, R], Heap));
       true ->
           {Heap, [[L, R] | NextInterval]}
    end.

filter(Q, nil) ->
    nil;
filter(Q, Heap) ->
    case top(Heap) of
        {K, [_, R]} ->
            if Q > R ->
                   {_, _, Heap1} = delete_min(Heap),
                   filter(Q, Heap1);
               true ->
                   Heap
            end
    end.

get_min(nil) ->
    -1;
get_min(Heap) ->
    {L, V} = top(Heap),
    L.

search_all([[Q, I] | NextQ], Intervals, Heap, Res) ->
    {Heap1, Intervals1} = possible(Q, Intervals, Heap),
    Heap2 = filter(Q, Heap1),
    search_all(NextQ, Intervals1, Heap2, [[get_min(Heap2), I] | Res]);
search_all([], _, _, Res) ->
    Res.

%% min heap

empty() ->
    nil.

-spec insert(K, V, Tree) -> p_tree()
    when K :: integer(),
         V :: any(),
         Tree :: p_tree().
insert(K, V, Tree) ->
    merge({K, V, nil, nil}, Tree).

-spec delete_min(Tree) -> {K, V, p_tree()}
    when Tree :: p_tree(),
         K :: integer(),
         V :: any().
delete_min(nil) ->
    {empty_error};
delete_min({K, V, C, nil}) ->
    {K, V, ccm_ccm(ccm(C, []), nil)}.

top(nil) ->
    nil;
top({K, V, _, nil}) ->
    {K, V}.

merge(nil, T2) ->
    T2;
merge(T1, nil) ->
    T1;
merge({K1, V1, C1, nil}, {K2, V2, C2, nil}) when K1 > K2 ->
    {K2, V2, {K1, V1, C1, C2}, nil};
merge({K1, V1, C1, nil}, {K2, V2, C2, nil}) ->
    {K1, V1, {K2, V2, C2, C1}, nil}.

% child_and_child_merge
ccm({K1, V1, C1, {K2, V2, C2, B}}, R) ->
    NewTree = merge({K1, V1, C1, nil}, {K2, V2, C2, nil}),
    ccm(B, [NewTree | R]);
ccm({K1, V1, C1, nil}, R) ->
    [{K1, V1, C1, nil} | R];
ccm(nil, R) ->
    R.

% child_and_child_merge result merge
ccm_ccm([T | NextT], ResultTree) ->
    ccm_ccm(NextT, merge(T, ResultTree));
ccm_ccm([], ResultTree) ->
    ResultTree.

